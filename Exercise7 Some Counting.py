#using for loop function

#creating a loop that counts up from 0 to 50 in increments of 1.
print("A loop that counts up from 0 to 50 in increments of 1")
for i in range(0,51,1):
    print(i)
print() #adding an empty print() statement so there will be space between set of statements.

#creating a loop that counts down from 50 to 0 in decrements of 1.
print("A loop that counts down from 50 to 0 in decrements of 1")
for i in range(50,-1,-1):
    print(i)
print()

